    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
    Hitung H = new Hitung();
    if ((txtKode.getText().equals(""))||(txtNama.getText().equals(""))||(txtHarga.getText().equals(""))||(txtJumlah.getText().equals(""))){
        JOptionPane.showMessageDialog(null, "Data Masih Kosong!","Pesan Error", JOptionPane.ERROR_MESSAGE);
    }else {
        H.setKode(txtKode.getText());
        H.setNama(txtNama.getText());
        H.setHarga(Double.parseDouble(txtHarga.getText()));
        H.setJumlah(Double.parseDouble(txtJumlah.getText()));
        txtSub.setText(String.valueOf(H.getSubTotal()));
        txtDiskon.setText(String.valueOf(H.Diskon()));
        txtPajak.setText(String.valueOf(H.getPajak()));
        txtTotal.setText(String.valueOf(H.Total()));
        btnHitung.setEnabled(true);
        btnSimpan.setEnabled(true);
        btnEdit.setEnabled(false);
        btnHapus.setEnabled(false);
        btnBatal.setEnabled(true);
    }
    }//GEN-LAST:event_btnHitungActionPerformed

