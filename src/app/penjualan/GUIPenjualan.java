    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
    st = cn.createStatement();
    if (edit) {
    st.executeUpdate("UPDATE penjualan set " +"kodebarang='"+ txtKode.getText() + "', "
    + "namabarang='" + txtNama.getText() + "'," + "harga='" + txtHarga.getText()+ "', "
    + "jumlah='" + txtJumlah.getText()+ "', "+ "subtotal='" + txtSub.getText() + "',"
    + "diskon='" + txtDiskon.getText() + "'," + "pajak='" + txtPajak.getText() + "',"
    + "total='" + txtTotal.getText() + "' WHERE kodebarang ='" + sId + "' ");
    }else {
    st.executeUpdate("INSERT INTO penjualan VALUES ('"+txtKode.getText()+"','"+txtNama.getText()+"','"+txtHarga.getText()+"','"+txtJumlah.getText()+"','"+txtSub.getText()+"','"+txtDiskon.getText()+"','"+txtPajak.getText()+"','"+txtTotal.getText()+"')"); // perintah untuk menginput data ke dalam database.
    }
    tampilData("");
    if (edit){
        JOptionPane.showMessageDialog(null, "Update Berhasil");
    }else {
        JOptionPane.showMessageDialog(null, "Simpan Berhasil");
    }
    refresh();
        } catch (Exception e) {
        e.printStackTrace();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

