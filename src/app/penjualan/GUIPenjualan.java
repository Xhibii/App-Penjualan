    public void autokode(){
        try {
            st =cn.createStatement();
            rs=st.executeQuery("Select count(kodetransaksi) from riwayat");
            if(rs.next()){
                int a=rs.getInt(1);
                    if (a<10){
                        txtKode.setText("TR-0000"+Integer.toString(a+1));
                }else if (a<100) {
                        txtKode.setText("TR-000"+Integer.toString(a+1));
                }else if (a<1000) {
                    txtKode.setText("TR-00"+Integer.toString(a+1));
                }else if (a<10000){
                    txtKode.setText("TR-0"+Integer.toString(a+1));
                }else if (a<100000){
                    txtKode.setText("TR-"+Integer.toString(a+1));
                }
            }
        } catch (Exception e) {
        }
    }
    
