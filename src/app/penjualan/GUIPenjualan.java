    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
            try{
            st = cn.createStatement();
            if (txtNama.getText().equals("")||txtJumlah.getText().equals("")||txtHarga.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Masih ada Data Kosong!");
            }else{
                  Hitung H = new Hitung();
                  H.setHarga(Double.parseDouble(txtHarga.getText()));
                  H.setJumlah(Double.parseDouble(txtJumlah.getText()));
                  subtot=H.getSubTotal();
            if(edit){
                st.executeUpdate("UPDATE penjualan set "+"jumlah='"+txtJumlah.getText()+"',"+"subtotal='"+subtot+"',"+"nama='"+txtNama.getText()+"'WHERE nomor='"+txtNomor.getText()+"'");
                JOptionPane.showMessageDialog(null,"Berhasil Ter-Update");
                 tampilData("");
                 Refresh();
            }
            else{
                st.executeUpdate("INSERT INTO penjualan(nomor,namabarang,jumlah,harga,subtotal) VALUES('"+txtNomor.getText()+"','"+txtNama.getText()+"','"
                                +txtJumlah.getText()+"','"+txtHarga.getText()+"','"+subtot+"')");
                JOptionPane.showMessageDialog(null,"Berhasil Disimpan");
                tampilData("");
                Refresh();
                }
        }
            }catch(Exception e){
            e.printStackTrace();
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

