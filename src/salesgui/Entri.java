    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
    //jika btnhitung ditekan
    SalesLogic S = new SalesLogic();
    if ((jTextField1.getText().equals("")) || (jTextField2.getText().equals("")) || (jTextField3.getText().equals("")) || (jTextField4.getText().equals(""))) {
    JOptionPane.showMessageDialog(null, "Data Masih Kosong!","Pesan Error", JOptionPane.ERROR_MESSAGE);
    }else{
    S.setId(jTextField1.getText());
    S.setNama(jTextField2.getText());
    S.setWilayah(jTextField3.getText());
    S.setPoint(Double.parseDouble(jTextField4.getText()));
    S.setGajiPokok(Double.parseDouble(jTextField5.getText()));
    jTextField6.setText(String.valueOf(S.Bonus()));
    jTextField7.setText(String.valueOf(S.Total()));
    btnHitung.setEnabled(true);
    btnSimpan.setEnabled(true);
    btnEdit.setEnabled(false);
    btnHapus.setEnabled(false);
    btnBatal.setEnabled(true);
    }
    }//GEN-LAST:event_btnHitungActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    try {
    st = cn.createStatement();
    if (edit) {
    st.executeUpdate("UPDATE datasales set " +"id='"+ jTextField1.getText() + "', "
    + "nama='" + jTextField2.getText() + "'," + "wilayah='" + jTextField3.getText()+ "', "
    + "point='" + jTextField4.getText()+ "', "+ "gajipokok='" + jTextField5.getText() + "',"
    + "bonus='" + jTextField6.getText() + "'," + "totalgaji='" + jTextField7.getText()+ "' WHERE id ='" + sId + "' "); // perintah untuk mengupdate data ke database dengan berpatokan pada id = sId.
    }else {
    st.executeUpdate("INSERT INTO datasales VALUES ('"+jTextField1.getText()+"','"+jTextField2.getText()+"','"+jTextField3.getText()+"','"+jTextField4.getText()+"','"+jTextField5.getText()+"','"+jTextField6.getText()+"','"+jTextField7.getText()+"')"); // perintah untuk menginput data ke dalam database.
    }
    tampilData("");
    if (edit){
        JOptionPane.showMessageDialog(null, "Update Berhasil");
    }else {
        JOptionPane.showMessageDialog(null, "Simpan Berhasil");
    }
    refresh();
        } catch (Exception e) {
        e.printStackTrace();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

