    public void tampilData(String where) { 
    try {
    st = cn.createStatement();
    tabModel.getDataVector().removeAllElements();
    tabModel.fireTableDataChanged();
    rs = st.executeQuery("SELECT * FROM datasales " + where);
    while (rs.next()) {
    Object[] data = {
    rs.getString("id"),
    rs.getString("nama"),
    rs.getString("wilayah"),
    rs.getString("point"),
    rs.getString("gajipokok"),
    rs.getString("bonus"),
    rs.getString("total"),
    };
    tabModel.addRow(data);
    }
    }catch(Exception e) {
    e.printStackTrace();
        }
    }
