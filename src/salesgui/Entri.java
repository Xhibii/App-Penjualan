    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
    try {
    int jawab;
    if ((jawab = JOptionPane.showConfirmDialog(null, "Ingin menghapus data?", "konfirmasi", JOptionPane.YES_NO_OPTION)) == 0) { // konfirmasi untuk menghapus data.
    st = cn.createStatement();
    st.executeUpdate("DELETE FROM datasales WHERE id='"+ tabModel.getValueAt(jTable1.getSelectedRow(), 0) + "'"); // perintah sql untuk menghapus data dimana id = baris yang di pilih.
    tampilData(""); // tampil data.
    refresh(); // memanggil metode refresh.
    }
    } catch (Exception e) {
    e.printStackTrace();
    }
    }//GEN-LAST:event_btnHapusActionPerformed

